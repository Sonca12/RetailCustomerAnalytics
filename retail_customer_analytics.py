"""## 6. Feature Engineering: Phân tích RFM

**RFM** là kỹ thuật kinh điển và được sử dụng rộng rãi nhất trong ngành retail/e-commerce (kể cả tại các doanh nghiệp Việt Nam) để định lượng giá trị khách hàng:

- **Recency (R):** Số ngày kể từ lần mua gần nhất → khách hàng còn "active" không
- **Frequency (F):** Số lần mua hàng → mức độ trung thành
- **Monetary (M):** Tổng chi tiêu → giá trị mang lại

Từ 3 chỉ số này, ta chấm điểm (1-4) theo quartile rồi gộp thành **RFM Score** để phân loại khách hàng thành các nhóm có ý nghĩa kinh doanh.

"""

snapshot_date = df["InvoiceDate"].max() + pd.Timedelta(days=1)

rfm = df.groupby("CustomerID").agg(
    Recency=("InvoiceDate", lambda x: (snapshot_date - x.max()).days),
    Frequency=("InvoiceNo", "nunique"),
    Monetary=("Revenue", "sum"),
).reset_index()

rfm["R_score"] = pd.qcut(rfm["Recency"], 4, labels=[4,3,2,1], duplicates="drop").astype(int)
rfm["F_score"] = pd.qcut(rfm["Frequency"].rank(method="first"), 4, labels=[1,2,3,4]).astype(int)
rfm["M_score"] = pd.qcut(rfm["Monetary"], 4, labels=[1,2,3,4], duplicates="drop").astype(int)
rfm["RFM_Score"] = rfm["R_score"] + rfm["F_score"] + rfm["M_score"]

def rfm_segment(score):
    if score >= 10: return "Champions"
    elif score >= 8: return "Loyal Customers"
    elif score >= 6: return "Potential Loyalist"
    elif score >= 4: return "At Risk"
    else: return "Lost / Hibernating"

rfm["Segment"] = rfm["RFM_Score"].apply(rfm_segment)

print(rfm["Segment"].value_counts())
rfm.head()

fig = px.pie(rfm, names="Segment", title="Phân bổ khách hàng theo phân khúc RFM", hole=0.4)
fig.write_html("outputs/figures/rfm_segment_pie.html")
fig.show()

"""## 7. Phân khúc khách hàng bằng Machine Learning (KMeans Clustering)

Ngoài RFM rule-based, ta dùng **KMeans clustering** — một kỹ thuật unsupervised ML phổ biến trong DA/DS — để tự động tìm nhóm khách hàng có hành vi tương tự, khách quan hơn so với việc đặt ngưỡng thủ công.

"""

X = rfm[["Recency","Frequency","Monetary"]].copy()
X_log = np.log1p(X)  # giảm skew vì Monetary/Frequency thường lệch phải mạnh

scaler = StandardScaler()
X_scaled = scaler.fit_transform(X_log)

sil_scores = {}
for k in range(2, 8):
    km_tmp = KMeans(n_clusters=k, random_state=42, n_init=10).fit(X_scaled)
    sil_scores[k] = silhouette_score(X_scaled, km_tmp.labels_)

plt.figure(figsize=(8,4))
plt.plot(list(sil_scores.keys()), list(sil_scores.values()), marker="o")
plt.title("Silhouette Score theo số cụm k")
plt.xlabel("k"); plt.ylabel("Silhouette Score")
plt.savefig("outputs/figures/silhouette_scores.png", dpi=150)
plt.show()

best_k = max(sil_scores, key=sil_scores.get)
print(f"Số cụm tối ưu (silhouette cao nhất): k = {best_k}")

kmeans = KMeans(n_clusters=best_k, random_state=42, n_init=10)
rfm["Cluster"] = kmeans.fit_predict(X_scaled)

cluster_summary = rfm.groupby("Cluster")[["Recency","Frequency","Monetary"]].mean().round(1)
cluster_summary["Số khách hàng"] = rfm["Cluster"].value_counts().sort_index()
print(cluster_summary)

# Trực quan hóa cụm bằng PCA (giảm 3 chiều RFM về 2D để vẽ)
pca = PCA(n_components=2, random_state=42)
coords = pca.fit_transform(X_scaled)
rfm["PC1"], rfm["PC2"] = coords[:,0], coords[:,1]

fig = px.scatter(rfm, x="PC1", y="PC2", color=rfm["Cluster"].astype(str),
                  hover_data=["Recency","Frequency","Monetary"],
                  title=f"Phân khúc khách hàng theo KMeans (k={best_k}) - chiếu qua PCA")
fig.write_html("outputs/figures/customer_clusters.html")
fig.show()

joblib.dump(kmeans, "outputs/models/kmeans_customer_segments.pkl")
joblib.dump(scaler, "outputs/models/rfm_scaler.pkl")
