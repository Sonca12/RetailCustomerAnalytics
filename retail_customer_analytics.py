def generate_synthetic_dataset(n_rows=25000, seed=42):
    """
    Sinh dữ liệu retail mô phỏng ĐÚNG cấu trúc cột của Online Retail dataset,
    dùng khi không thể kết nối tới UCI. Giúp notebook luôn chạy được (fail-safe).
    """
    rng = np.random.default_rng(seed)
    n_customers, n_products = 800, 200
    customers = [f"{13000+i}" for i in range(n_customers)]
    countries = ["United Kingdom","France","Germany","Spain","Netherlands",
                 "EIRE","Belgium","Portugal","Australia","Norway"]
    country_w = [0.60,0.08,0.08,0.05,0.05,0.04,0.03,0.03,0.02,0.02]
    stock_codes = [f"{10000+i}" for i in range(n_products)]
    descriptions = [f"PRODUCT {i}" for i in range(n_products)]

    period_start = pd.Timestamp("2019-12-01")
    period_end = pd.Timestamp("2020-12-09")
    total_hours = int((period_end - period_start) / pd.Timedelta(hours=1))

    # Mỗi khách hàng có 1 vòng đời: ngày bắt đầu mua và ngày ngừng mua (mô phỏng churn thật,
    # thay vì hoạt động đều suốt cả năm - giúp các bước Cohort và Churn phía sau có ý nghĩa
    cust_start = rng.integers(0, int(total_hours * 0.6), n_customers)
    is_churner = rng.random(n_customers) < 0.35
    cust_end = np.where(
        is_churner,
        cust_start + rng.integers(24, int(total_hours * 0.5), n_customers),
        np.full(n_customers, total_hours),
    )
    cust_end = np.minimum(cust_end, total_hours)

    idx_cust = rng.integers(0, n_customers, n_rows)
    idx_prod = rng.integers(0, n_products, n_rows)

    starts = cust_start[idx_cust]
    ends = np.maximum(cust_end[idx_cust], starts + 1)
    hour_offsets = (rng.random(n_rows) * (ends - starts)).astype(int)
    hour_idx = np.clip(starts + hour_offsets, 0, total_hours - 1)
    invoice_dates = period_start + pd.to_timedelta(hour_idx, unit="h")

    df_syn = pd.DataFrame({
        "InvoiceNo": [f"5{rng.integers(10000,99999)}" for _ in range(n_rows)],
        "StockCode": np.array(stock_codes)[idx_prod],
        "Description": np.array(descriptions)[idx_prod],
        "Quantity": rng.integers(1, 30, n_rows),
        "InvoiceDate": invoice_dates,
        "UnitPrice": np.round(rng.uniform(0.5, 50, n_rows), 2),
        "CustomerID": np.array(customers)[idx_cust],
        "Country": rng.choice(countries, n_rows, p=country_w),
    })

    # Mô phỏng đơn hủy (Invoice bắt đầu bằng 'C') và giá trị thiếu, giống dữ liệu thật
    cancel_mask = rng.random(n_rows) < 0.02
    df_syn.loc[cancel_mask, "InvoiceNo"] = "C" + df_syn.loc[cancel_mask, "InvoiceNo"]
    df_syn.loc[cancel_mask, "Quantity"] = -df_syn.loc[cancel_mask, "Quantity"]

    missing_mask = rng.random(n_rows) < 0.03
    df_syn.loc[missing_mask, "CustomerID"] = np.nan

    return df_syn


